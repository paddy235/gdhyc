CREATE OR REPLACE FUNCTION GETHETBZ(LNGZHIBM IN VARCHAR2,
                                    HETBID  IN VARCHAR2) RETURN NUMBER IS
  RESULT NUMBER(10, 2);
BEGIN
  RESULT := 0;
  select zhibz
    INTO RESULT
    from (               select to_char(cast(htzl.zhibdm as nvarchar2(10))) zhibdm,
                 htzl.zhibz
        from rl_gys_hetb ht, rl_gys_hetzlb htzl
           where ht.id = htzl.hetb_id
             and ht.id = HETBID
          union
          select 'SL' as zhibdm, hetl as zhibz from rl_gys_hetb where id = HETBID)
   where zhibdm = LNGZHIBM;
  RETURN(NVL(RESULT, 0));
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RETURN 0;
  WHEN OTHERS THEN
    RETURN 0;
END GETHETBZ;
