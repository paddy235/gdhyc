CREATE OR REPLACE FUNCTION GETLAIMXXMX(LNGZHIBM IN VARCHAR2,
                                       GONGYSID IN VARCHAR2,
                                       HETKSRQ IN VARCHAR2,
                                       HETJSRQ IN VARCHAR2) RETURN NUMBER IS
  RESULT NUMBER(10, 2);
BEGIN
  RESULT := 0;
  select zhibz
    INTO RESULT
    from (select 'SL' as zhibdm, nvl(sum(c.maoz-c.pinz), 0) as zhibz
            from rl_ys_chepb c inner join RL_YS_CHEPB_QC q on c.id=q.CHEPB_ID
           where gongysb_id = GONGYSID
             and q.qingcsj between HETKSRQ and HETJSRQ
          union
          select 'STAR' as zhibdm,
                 decode(sum(c.JINGZ),0,0,round_new(sum(c.jingz * c.STAR) / sum(c.JINGZ), 2)) zhibz
            from VM_CHEPMX c
           where c.gongysb_id = GONGYSID
             and c.QINGCSJ between HETKSRQ and HETJSRQ
           union

           select 'QNET_AR' as zhibdm,
                 decode(sum(c.JINGZ),0,0,round_new(sum(c.jingz * c.QNET_AR) / sum(c.JINGZ), 2)) zhibz
            from VM_CHEPMX c
           where c.gongysb_id = GONGYSID
             and c.QINGCSJ between HETKSRQ and HETJSRQ)
   where zhibdm = LNGZHIBM;
  RETURN(NVL(RESULT, 0));
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RETURN 0;
  WHEN OTHERS THEN
    RETURN 0;
END GETLAIMXXMX;
